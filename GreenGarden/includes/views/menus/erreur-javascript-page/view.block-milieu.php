<center>
<h2>Comment réactiver <b>javascript</b> sur le système ?</h2>
<h5>
<br />
<b>google chrome</b>
<br /><br />
- Ouvrez Chrome.<br />
- Dans la barre de menus, cliquez sur l'icône menu. <br />
- Choisissez Paramètres.<br />
- Dans le bas de la page, cliquez sur Afficher les paramètres avancés...<br />
- Dans Confidentialité, cliquez sur le bouton Paramètres de contenu...<br />
- Dans la fenêtre qui ouvre, allez à JavaScript et cochez l'option Autoriser tous les sites à exécuter JavaScript (recommandé).<br />
- Fermez l'onglet.<br />
- Actualisez la page si nécessaire (F5).<br />
<br /><br />
<b>firefox (versions antérieures à 23.0)</b>
<br /><br />
- Ouvrez Firefox.<br />
- Dans la barre de menus, cliquez sur Outils.<br />
- Choisissez Options.<br />
- Cliquez sur Contenu dans le haut de la fenêtre.<br />
- Cochez la case Activer JavaScript.<br />
- Précisez les paramètres dans Avancé... si désiré.<br />
- Cliquez sur OK.<br />
- Actualisez la page si nécessaire (F5).<br />
<br /><br />
<b>opera</b>
<br /><br />
- Lancez Opera.<br />
- Cliquez sur le menu Outils (ou sur le bouton Opéra, selon la version du navigateur).<br />
- Choisissez Préférences (ou Réglages, Préférences).<br />
- Dans la fenêtre qui ouvre, cliquez sur l'onglet Avancé.<br />
- Cliquez sur Contenu dans la liste de gauche.<br />
- Cochez Activer le JavaScript.<br />
- Options JavaScript... si désiré.<br />
- Cliquez sur OK.<br />
- Actualisez la page si nécessaire (F5).<br />
<br /><br />
<b>opera (versions récentes)</b>
<br /><br />
- Lancez Opera.<br />
- Cliquez sur le bouton Opéra.<br />
- Choisissez Réglages.<br />
- Dans l'onglet qui ouvre, cliquez sur Sites web dans la colonne de gauche.<br />
- Sous l'item JavaScript, cochez Permettre à tous les sites de lancer JavaScript (recommandé).<br />
- Gérer les exceptions... si désiré.<br />
- Fermez l'onglet.<br />
- Actualisez la page si nécessaire (F5).<br />
<br /><br />
<b>safari</b>
<br /><br />
- Ouvrez Safari.<br />
- Dans la barre de menus, cliquez sur Édition.<br />
- Choisissez Préférences...<br />
- Cliquez sur Sécurité dans le haut de la fenêtre.<br />
- Dans Contenu web, cochez la case Activer JavaScript.<br />
- Fermez la fenêtre.<br />
- Actualisez la page si nécessaire (F5).<br />
</h5>
